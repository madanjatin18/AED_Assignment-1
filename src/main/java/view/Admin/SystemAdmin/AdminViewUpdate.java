    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblDetails.getModel();
        model.setRowCount(0);
        
        for (Admins c : MainJFrame.adminDirectory.getAdminsList()){
            Object[] row = new Object[14];
            row[0] = c.getName();
            row[4] = c.getAge();
            row[6] = c.getGender();
            row[3] = c.getPhoneNumber();
            row[1] = c.getEmail();
            row[2] = c.getUsername();
            row[5] = c.getPassword();
            row[7] = c;
            
            model.addRow(row);
        }
    }
    
